    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre=txtNombre.getText().trim();
        String telefono=txtTelefono.getText().trim();
        String correo=txtCorreo.getText().trim();
        
        //verifica que todos los campos esten llenos
        if(nombre==null|| nombre.isEmpty()||telefono==null||telefono.isEmpty()||correo==null||correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Todos los datos son obligatorios", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //verifica que el correo tenga '@'
        if (correo.indexOf('@')==-1) {
            JOptionPane.showMessageDialog(this, "El correo debe de tener '@'", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //valida el prefijo del telefono
        if(!(telefono.startsWith("606")||telefono.startsWith("30"))){
        JOptionPane.showMessageDialog(this, "El telefono debe iniciar con '606 o '30'", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        Amigo nuevoamigo=new Amigo(nombre,telefono,correo);
        agenda.agregarAmigo(nuevoamigo);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

